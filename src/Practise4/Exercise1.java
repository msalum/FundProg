package Practise4;

public class Exercise1 {

    // Print out numbers on one line from 10 to 1

    public static void main(String[] args) {

        // for loop
        // where to start the loop from
        // how the loop should behave after one increment ( decrement)

        for (int i = 10; i > 0; i--) {

            System.out.print(i + " ");


        }
    }
}

